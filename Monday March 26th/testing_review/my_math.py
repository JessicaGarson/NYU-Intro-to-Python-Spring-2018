def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return float(a) / b


def main():
    print(add(a=3, b=6))
    print(subtract(a=5, b=2))
    print(multiply(a=3, b=6))
    print(divide(a=5, b=2))


if __name__ == "__main__":
    main()
